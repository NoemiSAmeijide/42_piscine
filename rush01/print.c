#include <unistd.h>

void	print(int board[4][4])
{
	int		i;
	int		j;
	int		b;

	i = 0;
	while (i < 4)
	{
		j = 0;
		while (j < 4)
		{
			b = board[i][j] + '0';
			write (1, &b, 1);
			if (j != 3)
				write (1, " ", 1);
			j++;
		}
		write (1, "\n", 1);
		i++;
	}
}
