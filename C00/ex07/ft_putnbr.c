#include <unistd.h>

void	ft_putnbr(int nb)
{
	long	l;
	char	c;

	l = nb;
	if (nb == 0)
	{
		write (1, "0", 1);
		return ;
	}
	if (nb < 0)
	{
		write (1, "-", 1);
		l *= -1;
	}
	if (l / 10)
	{
		ft_putnbr(l / 10);
	}
	c = l % 10 + 48;
	write (1, &c, 1);
}
